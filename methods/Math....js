//Math.floor
  //rounds num down to whole interger
(Math.floor(1.8))
  //--> 1
(Math.floor(3.3))
  // --> 3

//Math.ceil
  //rounds num up to whole interger
(Math.ceil(1.5))
//--> 2
(Math.ceil(3.3))
// --> 4


//Math.random
  //generates random number
(Math.random());
  // --> [some random number between 0 - 1]


//Math.sign
  //returns 1 if number is positive; 0 if a num is zero; and -1 if a number is negative;
(Math.sign(-5));
  // --> -1

//Math.max
  //returns highest number
Math.max(1,4,2);
// --> 4

//Math.min
  //returns lowest number
Math.min(1,4,2);
  // --> 1

//Math.pow
  //return ar1 to the power of arg2
Math.pow(2,4)
  // --> 16
